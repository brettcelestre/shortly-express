// Checks to make sure you're logged in
exports.restrict = function(req, res, callback){
  if ( req.session.user ){
    console.log('restrict success');
    callback();
  } else {
    console.log('restrict error url - ', req.url);
    req.session.error = 'Access denied';
    res.redirect('/login');
  }
};

exports.passwordHash = function (password) {
  var hash = crypto.createHash('sha1');
  hash.update(password);
  return hash.digest('hex').slice(0, 5);
};

